#include "ft_printf.h"

int	ft_u(unsigned int c)
{
	int	j;

	j = 0;
	if (c >= 10)
		j += ft_u(c / 10);
	c = (c % 10) + 48;
	j += write(1, &c, 1);
	return (j);
}
