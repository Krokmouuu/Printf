#include "ft_printf.h"

int	ft_x(unsigned int n, int a)
{
	char	*str;
	char	c;
	int		j;

	j = 0;
	if (a == 0)
		str = "0123456789abcdef";
	else
		str = "0123456789ABCDEF";
	if (n >= 16)
	{
		j += ft_x(n / 16, a);
		j += ft_x(n % 16, a);
	}
	if (n < 16)
	{
		c = str[n];
		write(1, &c, 1);
		j++;
	}
	return (j);
}
