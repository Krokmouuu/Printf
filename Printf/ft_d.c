#include "ft_printf.h"

int	ft_d(int c)
{
	int	j;

	j = 0;
	if (c == -2147483648)
	{
		return (write(1, "-2147483648", 11));
	}
	if (c < 0)
	{
		j += write(1, "-", 1);
		c = -c;
	}
	if (c >= 10)
	{
		j += ft_d(c / 10);
	}
	c = (c % 10) + 48;
	write(1, &c, 1);
	j++;
	return (j);
}
